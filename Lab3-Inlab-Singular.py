NicoleAnne = {0, 22, 33, 55, 99};
Mejia= {22, 33, 55, 99, 100};

# union
print("\nThe union between the two sets:", NicoleAnne| Mejia)

# intersection
print("\nThe intersection of two sets:", NicoleAnne & Mejia)

# difference
print("\nThe difference between two sets:", NicoleAnne - Mejia)

# symmetric difference
print("\nThe symmetric difference:", NicoleAnne ^ Mejia)